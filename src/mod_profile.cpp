void ProfileModule::resetCounts()
{
	for(int i = 0; i < 1700; i++) {
		instructionCounts[i] = 0;
		for(int n=0;n<3;n++) {
			instructionBufferSizes[i] = 0;
		}
	}
}


void ProfileModule::output()
{
	int values[5] = {0, 0, 0, 0, 0};
	int instructions[5] = {0, 0, 0, 0, 0};
	for(int i = 0; i < 1700; i++) {
		int count = instructionCounts[i];
		if(count > 0) {
			if(count > values[4]) {
				values[0] = values[1];
				values[1] = values[2];
				values[2] = values[3];
				values[3] = values[4];
				values[4] = count;
				instructions[0] = instructions[1];
				instructions[1] = instructions[2];
				instructions[2] = instructions[3];
				instructions[3] = instructions[4];
				instructions[4] = i;
			}
			else if(count > values[3]) {
				values[0] = values[1];
				values[1] = values[2];
				values[2] = values[3];
				values[3] = count;
				instructions[0] = instructions[1];
				instructions[1] = instructions[2];
				instructions[2] = instructions[3];
				instructions[3] = i;
			}
			else if(count > values[2]) {
				values[0] = values[1];
				values[1] = values[2];
				values[2] = count;
				instructions[0] = instructions[1];
				instructions[1] = instructions[2];
				instructions[2] = i;
			}
			else if(count > values[1]) {
				values[0] = values[1];
				values[1] = count;
				instructions[0] = instructions[1];
				instructions[1] = i;
			}
			else if(count > values[0]) {
				values[0] = count;
				instructions[0] = i;
			}
		}
	}
	int count = 1;
	LOG("\n");
	for(int i = 4; i >= 0; i--) {
		LOG("%d. Instruction: %s called %d times!\n", count, strings[instructions[i]], values[i]);
		count++;
	}
	LOG("\n");
	outputBuffers();
}


