bool ProfileModule::process(vector<Instruction *> *list)
{

	for(int i=0;i<(int)list->size();i++){
		Instruction *iter = (*list)[i];
		instructionCounts[iter->id]++;
		for(int n=0;n<3;n++) {
			instructionBufferSizes[iter->id] += iter->buffers[n].len;
		}
	}

	return true;
}


